    private void loginBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginBtnActionPerformed
       
        String usertype  = userTypeCB.getSelectedItem().toString();
        String username  = usernameTF.getText();
        String password  = passwordTF.getText();
        
                   String dburl = "jdbc:oracle:thin:@localhost:1521:xe";
                   String user = "SYSTEM";
                   String pass = "1234";

        try{
            Connection con = DriverManager.getConnection(dburl,user,pass);
            System.out.println("connected successfully");
            PreparedStatement ps = con.prepareStatement("select * from BANK_CE_TABLE where user_type='"+usertype+"' and username='"+username+"' and password="+password+"");
            
        ResultSet rs = ps.executeQuery();
        
        if(rs.next())
                {
                    this.setVisible(false);
                    String u_name = usernameTF.getText();
                    if(usertype.equals("Employee"))
                        new Employee(u_name).setVisible(true);
                    else
                        new Customer(u_name).setVisible(true);  
                    
                }
        else
        {
            JOptionPane.showMessageDialog(this,"Invalid Usertype, Username and Password");
            usernameTF.setText("");
            passwordTF.setText("");
            usernameTF.requestFocus();
        }
        }
      catch (SQLException ex) {
            System.out.println("Error");
            Logger.getLogger(Register.class.getName()).log(Level.SEVERE, null, ex);
        }      
            
            
            
    }//GEN-LAST:event_loginBtnActionPerformed

