    private void userTypeCBItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_userTypeCBItemStateChanged
        int index = userTypeCB.getSelectedIndex();
        if (index != 0) {
            usernameTF.setEnabled(true);
            passwordTF.setEnabled(true);
            showHideCheckBox.setEnabled(true);
            loginBtn.setEnabled(true);
            clearBtn.setEnabled(true);
        } else {
            usernameTF.setEnabled(false);
            passwordTF.setEnabled(false);
            showHideCheckBox.setEnabled(false);
            loginBtn.setEnabled(false);
            clearBtn.setEnabled(false);
        }
    }//GEN-LAST:event_userTypeCBItemStateChanged

